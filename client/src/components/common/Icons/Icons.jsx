// Helper to get recording type icon
export const getRecordingTypeIcon = (type, size = 20) => {
  switch (type) {
    case 'video': return <VideoIcon size={size} />;
    case 'audio': return <MicIcon size={size} />;
    case 'text': return <TextIcon size={size} />;
    default: return null;
  }
};
